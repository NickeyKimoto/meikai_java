package MeikaiJava.Chap05;

/*
*クラス名：E_05_03
*概要：論理型の変数にtrueやfalseを代入して、その値を表示するプログラム
*作成者：N.Kimoto
*作成日：2024/04/11
*/

public class E_05_03 {

	/*
	*関数名：main
	*概要：論理型の変数にtrueやfalseを代入して、その値を表示する
	*引数：なし
	*戻り値：なし
	*作成者：N.Kimoto
	*作成日：2024/04/11
	*/

	public static void main(String[] args) {
		
		// 論理型の変数にtrueを代入
		boolean trueValue = true;
		// 論理型の変数にfalseを代入
		boolean falseValue = false;
		
		// 論理型の変数trueValueの値を表示
		System.out.println("trueValueの値は" + trueValue + "です。");
		// 論理型の変数falseValueの値を表示
		System.out.println("falseValueの値は" + falseValue + "です。");

	}

}
